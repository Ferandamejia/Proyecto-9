
function setup() {
  createCanvas(400,400);
}

function draw() 
{

 if (keyIsDown(RIGHT_ARROW)){
background ("DarkSeaGreen")
 }
if (keyIsDown(LEFT_ARROW)){
   background("RosyBrown")
 }

 if (keyIsDown(UP_ARROW)){
   background("DarkSlateGray")
}

if (keyIsDown(DOWN_ARROW)){
   background("DarkSalmon")
}
}




